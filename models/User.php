    public static function isUserAdmin($username)
    {
        if (
            static::findOne(['username' => $username, 'role' => self::ROLE_ADMIN])
            )
        {
            return true;
        }
        else {
            return false;
        }
    }

     public static function isUserUser($username)
    {
        if (
            static::findOne(['username' => $username, 'role' => self::ROLE_USER])
            )
        {
            return true;
        }
        else {
            return false;
        }
    }


}
